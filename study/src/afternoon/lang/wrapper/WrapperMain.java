package afternoon.lang.wrapper;

public class WrapperMain {
    public static void main(String[] args) {
        Integer a = 10;
        Integer b = 20;

        Long c = 10L;
        Double d = 20.5;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
